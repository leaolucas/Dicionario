    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed

        ArrayList<String> al = new ArrayList();

        al = c.buscarExpressoes(informacao.getText().toString(), opcoesSelect.getSelectedIndex());

        listaExpressoes.setModel(dm);
        dm.clear();
        dm.addElement("Buscar por: " + opcoesSelect.getSelectedItem() + " > " + informacao.getText().toString());
        for(String nome: al){
            dm.addElement(nome);
        }
    }//GEN-LAST:event_buscarActionPerformed

