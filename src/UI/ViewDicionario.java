    private void btnAvancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAvancarActionPerformed

        ArrayList<String> al = new ArrayList();

        /*String resultado = new metodo construtor que vai resolver das parada do alterar, inserir e verificar()*/
        String resultado = c.gerenciarAcoes(expressaoNova.getText().toString(), expressaoAntiga.getText().toString(),  selectAction.getSelectedIndex());
        infoRetorno.setText(resultado);
        infoRetorno.setForeground(Color.BLUE);

        al = c.getExpressoesDicionario();

        dicionario.setModel(dm);
        dm.clear();
        for(String nome: al){
            dm.addElement(nome);
        }
    }//GEN-LAST:event_btnAvancarActionPerformed

    private void selectActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionActionPerformed
        System.out.println(selectAction.getSelectedItem());
        if(selectAction.getSelectedItem().equals("Alterar")){
            expressaoAntigaLabel.setVisible(true);
            expressaoAntiga.setVisible(true);
            expressaoNovaLabel.setText("Expressão Nova");

        }
        else{
            expressaoAntigaLabel.setVisible(false);
            expressaoAntiga.setVisible(false);
            expressaoAntiga.setText("");
            expressaoNovaLabel.setText("Expressão");
        }
    }//GEN-LAST:event_selectActionActionPerformed

